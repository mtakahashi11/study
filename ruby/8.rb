# 継承とは
# 既に定義されているクラスを拡張して新しいクラスを作ることを継承と呼びます。
# is_a?メソッド
# サブクラスとスーパークラスの関係を「is-aの関係にある」といいます。
# オーバーライド
# サブクラスでスーパークラスのメソッドと同じ名前のメソッドを定義し直すことをオーバーライドと呼びます。
# superを使うことによって、その使われたメソッドがオーバーライドしているメソッドを呼び出します。
# superclassメソッド
# superclassメソッドを使うことによって、レシーバに指定したクラスのスーパークラスを調べることができます。
# アクセス制限
# メソッドの可視性を「public」「private」「protected」に設定できます。