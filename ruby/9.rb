# モジュールとは
# モジュールは手続きの部分だけをまとめたものです。
# 名前空間
# モジュールを使うことによって、メソッドと定数とクラスの名前がぶつかるのを防ぐことができます。
# include
# 定義しているクラスの中で使うことによって、引数に指定したモジュールの機能を渡せます。
# 特異メソッド
# 特定のオブジェクトで使うことができるメソッドのことです。
# extend
# 引数で指定したモジュールのインスタンスメソッドを特異メソッドとして、オブジェクトに渡せます。
# 組み込みモジュール
# Comparableモジュールは比較演算子を加えてくれるモジュールです。
# Enumerableモジュールは繰り返しの機能を加えてくれるモジュールです。